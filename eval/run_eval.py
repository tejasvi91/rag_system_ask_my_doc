import sys
import json
from pathlib import Path
from datetime import datetime

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from datasets import Dataset
from ragas import evaluate
from ragas.metrics import faithfulness, answer_relevancy
from langchain_openai import ChatOpenAI, OpenAIEmbeddings
from src.retrieval.hybrid_retriever import hybrid_search
from src.retrieval.reranker import rerank
from src.retrieval.retriever import format_context
from src.retrieval.answer_v2 import answer_question_v2
from src.config import OPENAI_API_KEY, CHAT_MODEL, EMBEDDING_MODEL

GOLDEN_DATASET_PATH = Path(__file__).parent / "golden_dataset.json"
RESULTS_PATH = Path(__file__).parent / "eval_results.json"
FAITHFULNESS_THRESHOLD = 0.7


def build_ragas_dataset(golden_dataset: list) -> Dataset:
    """
    Run each golden question through the Phase 2 pipeline and
    collect everything RAGAS needs to score:
    - question
    - answer generated by the pipeline
    - contexts (the actual chunks retrieved)
    - ground_truth (your manually verified answer)
    """
    questions = []
    answers = []
    contexts = []
    ground_truths = []

    for i, item in enumerate(golden_dataset):
        question = item["question"]
        ground_truth = item["ground_truth"]

        print(f"  [{i+1}/{len(golden_dataset)}] {question[:60]}...")

        # Get answer from Phase 2 pipeline
        result = answer_question_v2(question)
        answer = result["answer"]

        # Get the raw chunk texts for RAGAS context scoring
        candidates = hybrid_search(question, top_k=5)
        reranked = rerank(question, candidates, top_k=3)
        chunk_texts = [c.page_content for c in reranked]

        questions.append(question)
        answers.append(answer)
        contexts.append(chunk_texts)
        ground_truths.append(ground_truth)

    return Dataset.from_dict({
        "question": questions,
        "answer": answers,
        "contexts": contexts,
        "ground_truth": ground_truths,
    })


def run_evaluation():
    print("=== Phase 3 RAGAS Evaluation ===\n")

    # Load golden dataset
    with open(GOLDEN_DATASET_PATH, "r") as f:
        golden_dataset = json.load(f)
    print(f"Loaded {len(golden_dataset)} questions from golden dataset\n")

    # Build RAGAS dataset by running the pipeline
    print("Running pipeline on all questions...")
    ragas_dataset = build_ragas_dataset(golden_dataset)

    # Run RAGAS evaluation
    print("\nScoring with RAGAS...")
    llm = ChatOpenAI(
        model=CHAT_MODEL,
        openai_api_key=OPENAI_API_KEY
    )
    embeddings = OpenAIEmbeddings(
        model=EMBEDDING_MODEL,
        openai_api_key=OPENAI_API_KEY
    )

    results = evaluate(
        dataset=ragas_dataset,
        metrics=[faithfulness, answer_relevancy],
        llm=llm,
        embeddings=embeddings
    )

    # Extract scores
    scores = results.to_pandas()
    faithfulness_score = float(scores["faithfulness"].mean())
    relevancy_score = float(scores["answer_relevancy"].mean())

    print(f"\n=== Results ===")
    print(f"Faithfulness:     {faithfulness_score:.3f}")
    print(f"Answer relevancy: {relevancy_score:.3f}")
    print(f"Threshold:        {FAITHFULNESS_THRESHOLD}")

    # Determine pass/fail
    passed = faithfulness_score >= FAITHFULNESS_THRESHOLD
    status = "PASSED" if passed else "FAILED"
    print(f"Status:           {status}")

    # Save results to file — CI pipeline reads this
    output = {
        "timestamp": datetime.now().isoformat(),
        "faithfulness": faithfulness_score,
        "answer_relevancy": relevancy_score,
        "threshold": FAITHFULNESS_THRESHOLD,
        "passed": passed,
        "status": status,
        "per_question": scores[["question", "faithfulness", "answer_relevancy"]].to_dict(orient="records")
    }

    with open(RESULTS_PATH, "w") as f:
        json.dump(output, f, indent=2)

    print(f"\nResults saved to: {RESULTS_PATH}")

    # Exit with error code if failed — CI pipeline uses this
    if not passed:
        print(f"\nFaithfulness {faithfulness_score:.3f} is below threshold {FAITHFULNESS_THRESHOLD}")
        print("Build should fail.")
        sys.exit(1)

    return output


if __name__ == "__main__":
    run_evaluation()